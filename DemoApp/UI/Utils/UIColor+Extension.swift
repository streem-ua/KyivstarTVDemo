import UIKit

extension UIColor {
    static func appBlack() -> UIColor {
        return UIColor(resource: .appBlack)
    }
    
    static func appDarkBlack() -> UIColor {
        return UIColor(resource: .appDarkBlack)
    }
    
    static func appBlue() -> UIColor {
        return UIColor(resource: .appBlue)
    }
    
    static func appGray() -> UIColor {
        return UIColor(resource: .appGray)
    }
}
